import * as fs from "fs";
import * as path from "path";
import { fileURLToPath } from "url";
import { Document, parse, Scalar } from "yaml";

const BIN_DIR = path.dirname(fileURLToPath(import.meta.url));

type CategoryItem = {
  category: string;
};

type SuggestForOptions = {
  fieldName: string;
  filenameExample: string;
};

export type MarketplaceRequirements = {
  skills?: string[];
  vscode_extensions?: Array<{ name: string; id: string }>;
  mcps?: string[];
};

type SelfRequirement = {
  subgroup: "skills" | "mcps";
  id: string;
};

type GenerateMarketplaceOptions<T> = {
  rootDir: string;
  outputFile?: string;
  parseItem: (dirName: string) => T;
  sortItems?: (a: T, b: T) => number;
  header?: (items: T[]) => string | undefined;
  finalMessage: (count: number) => string;
};

