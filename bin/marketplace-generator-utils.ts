import * as fs from "fs";
import * as path from "path";
import { fileURLToPath } from "url";
import { Document, Scalar } from "yaml";

const BIN_DIR = path.dirname(fileURLToPath(import.meta.url));

type CategoryItem = {
  category: string;
};

type SuggestForOptions = {
  fieldName: string;
  filenameExample: string;
};

export function repoPathFromBin(...segments: string[]): string {
  return path.join(BIN_DIR, "..", ...segments);
}

export function listVisibleDirectories(rootDir: string): string[] {
  return fs
    .readdirSync(rootDir, { withFileTypes: true })
    .filter((d) => d.isDirectory() && !d.name.startsWith("."))
    .map((dir) => dir.name);
}

export function writeMarketplaceYaml(
  outputPath: string,
  items: unknown[],
  header?: string,
): void {
  const doc = new Document({ items });
  const yaml = doc.toString({ lineWidth: 120 });
  fs.writeFileSync(outputPath, header ? `${header}\n${yaml}` : yaml);
}

export function buildCategorySummary(
  items: CategoryItem[],
  options: {
    title: string;
    resourceNameSingular: string;
    resourceNamePlural: string;
    scriptName: string;
    singleCategoryAware?: boolean;
  },
): string {
  const categoryCounts = new Map<string, number>();
  for (const item of items) {
    categoryCounts.set(item.category, (categoryCounts.get(item.category) ?? 0) + 1);
  }

  const categoryWord =
    options.singleCategoryAware && categoryCounts.size === 1 ? "category" : "categories";

  return [
    `# ${options.title}`,
    "#",
    `# ${items.length} ${options.resourceNamePlural} across ${categoryCounts.size} ${categoryWord}. Each ${options.resourceNameSingular} has one primary category.`,
    `# This summary is generated by ${options.scriptName}; do not edit it manually.`,
    "#",
    ...[...categoryCounts]
      .sort(([categoryA, countA], [categoryB, countB]) =>
        countB - countA || categoryA.localeCompare(categoryB),
      )
      .map(([category, count]) => `# - ${category}: ${count}`),
    "#",
  ].join("\n");
}

export function requireString(value: unknown, field: string, file: string): string {
  if (typeof value !== "string" || value.trim().length === 0) {
    throw new Error(`${file}: missing required string field ${field}`);
  }
  return value;
}

export function foldedScalar(value: string): Scalar {
  const scalar = new Scalar(value);
  scalar.type = Scalar.BLOCK_FOLDED;
  scalar.blockChomping = "strip";
  return scalar;
}

export function validateSuggestFor(
  value: unknown,
  itemId: string,
  options: SuggestForOptions,
): unknown {
  if (value === undefined) return undefined;
  if (!value || typeof value !== "object" || Array.isArray(value)) {
    throw new Error(`${itemId}: ${options.fieldName} must be an object`);
  }

  const suggestFor = value as Record<string, unknown>;
  const unknownKey = Object.keys(suggestFor).find(
    (key) => key !== "filename" && key !== "vscode_extension",
  );
  if (unknownKey) {
    throw new Error(`${itemId}: ${options.fieldName} has unknown property "${unknownKey}"`);
  }

  const filenames = suggestFor.filename;
  const vscodeExtensions = suggestFor.vscode_extension;
  if (filenames === undefined && vscodeExtensions === undefined) {
    throw new Error(`${itemId}: ${options.fieldName} must contain filename or vscode_extension`);
  }

  if (
    filenames !== undefined &&
    (!Array.isArray(filenames) ||
      filenames.length === 0 ||
      !filenames.every(
        (filename) =>
          typeof filename === "string" &&
          /^\*\.[A-Za-z0-9]+(?:\.[A-Za-z0-9]+)*$/.test(filename),
      ))
  ) {
    throw new Error(
      `${itemId}: ${options.fieldName}.filename must be a non-empty list of patterns like "${options.filenameExample}"`,
    );
  }

  if (
    vscodeExtensions !== undefined &&
    (!Array.isArray(vscodeExtensions) ||
      vscodeExtensions.length === 0 ||
      !vscodeExtensions.every(
        (extensionId) =>
          typeof extensionId === "string" &&
          /^[A-Za-z0-9][A-Za-z0-9-]*\.[A-Za-z0-9][A-Za-z0-9-]*$/.test(extensionId),
      ))
  ) {
    throw new Error(
      `${itemId}: ${options.fieldName}.vscode_extension must be a non-empty list of extension IDs like "ms-toolsai.jupyter"`,
    );
  }

  return value;
}
