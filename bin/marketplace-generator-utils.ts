export function repoPathFromBin(...segments: string[]): string {
  return path.join(BIN_DIR, "..", ...segments);
}

export function listVisibleDirectories(rootDir: string): string[] {
  return fs
    .readdirSync(rootDir, { withFileTypes: true })
    .filter((d) => d.isDirectory() && !d.name.startsWith("."))
    .map((dir) => dir.name);
}

export function loadMcpIds(mcpsDir: string): Set<string> {
  const sourceById = new Map<string, string>();

  for (const dirName of listVisibleDirectories(mcpsDir)) {
    const file = path.join(mcpsDir, dirName, "MCP.yaml");
    const parsed = parse(fs.readFileSync(file, "utf-8")) as unknown;
    const id = requireString(
      parsed && typeof parsed === "object" && !Array.isArray(parsed)
        ? (parsed as Record<string, unknown>).id
        : undefined,
      "id",
      file,
    );
    const existingFile = sourceById.get(id);
    if (existingFile) {
      throw new Error(`${file}: duplicate MCP id "${id}" also declared in ${existingFile}`);
    }
    sourceById.set(id, file);
  }

  return new Set(sourceById.keys());
}

export function writeMarketplaceYaml(
  outputPath: string,
  items: unknown[],
  header?: string,
): void {
  const doc = new Document({ items });
  const yaml = doc.toString({ lineWidth: 120 });
  fs.writeFileSync(outputPath, header ? `${header}\n${yaml}` : yaml);
}

export function generateMarketplace<T>(options: GenerateMarketplaceOptions<T>): T[] {
  const items = listVisibleDirectories(options.rootDir).map(options.parseItem);
  if (options.sortItems) items.sort(options.sortItems);

  writeMarketplaceYaml(
    path.join(options.rootDir, options.outputFile ?? "marketplace.yaml"),
    items,
    options.header?.(items),
  );

  console.log(options.finalMessage(items.length));
  return items;
}

export function buildCategorySummary(
  items: CategoryItem[],
  options: {
    title: string;
    resourceNameSingular: string;
    resourceNamePlural: string;
    scriptName: string;
    singleCategoryAware?: boolean;
  },
): string {
  const categoryCounts = new Map<string, number>();
  for (const item of items) {
    categoryCounts.set(item.category, (categoryCounts.get(item.category) ?? 0) + 1);
  }

  const categoryWord =
    options.singleCategoryAware && categoryCounts.size === 1 ? "category" : "categories";

  return [
    `# ${options.title}`,
    "#",
    `# ${items.length} ${options.resourceNamePlural} across ${categoryCounts.size} ${categoryWord}. Each ${options.resourceNameSingular} has one primary category.`,
    `# This summary is generated by ${options.scriptName}; do not edit it manually.`,
    "#",
    ...[...categoryCounts]
      .sort(([categoryA, countA], [categoryB, countB]) =>
        countB - countA || categoryA.localeCompare(categoryB),
      )
      .map(([category, count]) => `# - ${category}: ${count}`),
    "#",
  ].join("\n");
}

export function requireString(value: unknown, field: string, file: string): string {
  if (typeof value !== "string" || value.trim().length === 0) {
    throw new Error(`${file}: missing required string field ${field}`);
  }
  return value;
}

const VSCODE_EXTENSION_ID_PATTERN = /^[A-Za-z0-9][A-Za-z0-9-]*\.[A-Za-z0-9][A-Za-z0-9-]*$/;

/** Returns true if `entry` is a valid `{ name, id }` VS Code extension object. */
export function isValidVscodeExtension(entry: unknown): boolean {
  if (!entry || typeof entry !== "object" || Array.isArray(entry)) return false;
  const extension = entry as Record<string, unknown>;
  return (
    Object.keys(extension).every((key) => key === "name" || key === "id") &&
    typeof extension.name === "string" &&
    extension.name.trim().length > 0 &&
    typeof extension.id === "string" &&
    VSCODE_EXTENSION_ID_PATTERN.test(extension.id)
  );
}

export function foldedScalar(value: string): Scalar {
  const scalar = new Scalar(value);
  scalar.type = Scalar.BLOCK_FOLDED;
  scalar.blockChomping = "strip";
  return scalar;
}

export function validateRequirements(
  value: unknown,
  itemId: string,
  skillIds: ReadonlySet<string>,
  mcpIds: ReadonlySet<string>,
  selfRequirement?: SelfRequirement,
): MarketplaceRequirements | undefined {
  if (value === undefined) return undefined;
  if (!value || typeof value !== "object" || Array.isArray(value)) {
    throw new Error(`${itemId}: requirements must be an object`);
  }

  const requirements = value as Record<string, unknown>;
  const supportedSubgroups = ["skills", "vscode_extensions", "mcps"] as const;
  const unknownKey = Object.keys(requirements).find(
    (key) => !supportedSubgroups.includes(key as (typeof supportedSubgroups)[number]),
  );
  if (unknownKey) {
    throw new Error(`${itemId}: requirements has unknown property "${unknownKey}"`);
  }
  if (Object.keys(requirements).length === 0) {
    throw new Error(`${itemId}: requirements must contain at least one supported subgroup`);
  }

  for (const subgroup of supportedSubgroups) {
    if (!Object.prototype.hasOwnProperty.call(requirements, subgroup)) continue;
    const entries = requirements[subgroup];
    if (!Array.isArray(entries) || entries.length === 0) {
      throw new Error(`${itemId}: requirements.${subgroup} must be a non-empty list`);
    }

    if (subgroup === "vscode_extensions") {
      if (!entries.every(isValidVscodeExtension)) {
        throw new Error(
          `${itemId}: requirements.vscode_extensions entries must contain a non-empty name and a valid extension ID like "ms-toolsai.jupyter"`,
        );
      }
      continue;
    }

    if (!entries.every((entry) => typeof entry === "string" && entry.trim().length > 0)) {
      throw new Error(
        `${itemId}: requirements.${subgroup} must be a non-empty list of non-empty strings`,
      );
    }

    if (
      selfRequirement?.subgroup === subgroup &&
      entries.some((entry) => entry === selfRequirement.id)
    ) {
      throw new Error(`${itemId}: requirements.${subgroup} must not reference itself`);
    }

    const availableIds = subgroup === "skills" ? skillIds : mcpIds;
    const unknownId = entries.find((entry) => !availableIds.has(entry));
    if (unknownId) {
      const resource = subgroup === "skills" ? "skill" : "MCP";
      throw new Error(
        `${itemId}: requirements.${subgroup} references unknown ${resource} ID "${unknownId}"`,
      );
    }
  }

  return value as MarketplaceRequirements;
}

export function validateSuggestFor(
  value: unknown,
  itemId: string,
  options: SuggestForOptions,
): unknown {
  if (value === undefined) return undefined;
  if (!value || typeof value !== "object" || Array.isArray(value)) {
    throw new Error(`${itemId}: ${options.fieldName} must be an object`);
  }

  const suggestFor = value as Record<string, unknown>;
  const unknownKey = Object.keys(suggestFor).find(
    (key) => key !== "filename" && key !== "vscode_extension",
  );
  if (unknownKey) {
    throw new Error(`${itemId}: ${options.fieldName} has unknown property "${unknownKey}"`);
  }

  const filenames = suggestFor.filename;
  const vscodeExtensions = suggestFor.vscode_extension;
  if (filenames === undefined && vscodeExtensions === undefined) {
    throw new Error(`${itemId}: ${options.fieldName} must contain filename or vscode_extension`);
  }

  if (
    filenames !== undefined &&
    (!Array.isArray(filenames) ||
      filenames.length === 0 ||
      !filenames.every(
        (filename) =>
          typeof filename === "string" &&
          /^\*\.[A-Za-z0-9]+(?:\.[A-Za-z0-9]+)*$/.test(filename),
      ))
  ) {
    throw new Error(
      `${itemId}: ${options.fieldName}.filename must be a non-empty list of patterns like "${options.filenameExample}"`,
    );
  }

  if (vscodeExtensions !== undefined) {
    if (!Array.isArray(vscodeExtensions) || vscodeExtensions.length === 0) {
      throw new Error(
        `${itemId}: ${options.fieldName}.vscode_extension must be a non-empty list`,
      );
    }
    if (!vscodeExtensions.every(isValidVscodeExtension)) {
      throw new Error(
        `${itemId}: ${options.fieldName}.vscode_extension entries must contain a non-empty name and a valid extension ID like "ms-toolsai.jupyter"`,
      );
    }
  }

  return value;
}
